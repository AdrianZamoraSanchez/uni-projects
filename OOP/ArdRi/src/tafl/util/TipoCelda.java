package tafl.util;
/**
 * Enumeración que implementa los tipos de celdas
 * 
 * @author <a href="azs1004@alu.ubu.es">Adrián Zamora Sánchez</a>
 * @see tafl.modelo.Celda
 * @version 1.0
 * @since 1.0
 * 
*/
public enum TipoCelda {
	
	/**
	 * Celda donde comienza el rey y en la que solo puede estar el rey
	 */
	TRONO,
	/**
	 * Celdas donde solo puede estar el rey
	 */
	PROVINCIA,
	
	/**
	 * Celda normal
	 */
	NORMAL;
}
	
	
